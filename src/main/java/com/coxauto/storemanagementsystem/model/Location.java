package com.coxauto.storemanagementsystem.model;

public class Location {
	float lat;
    float lon;
    
	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public float getLon() {
		return lon;
	}
	public void setLon(float lon) {
		this.lon = lon;
	}
}
